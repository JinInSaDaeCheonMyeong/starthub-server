            이 형식을 사용하면 사용자가 해당 항목으로 바로 이동할 수 있는 버튼이 제공됩니다.
            사용자의 맥락에서 BMC ID, 분석 ID, 공고 ID를 확인하여 정확하게 참조하세요.
            일정에 추가된 공고를 언급할 때는 SCHEDULE 형식을 사용하세요.
            """.trimIndent()
    }

